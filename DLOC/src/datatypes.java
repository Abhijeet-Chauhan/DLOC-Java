
public class datatypes {
	public static void main(String[] args) {
		int a = 10; //integer 4 bytes
		float b = 10.10f; //float 4 byte
		double c = 10.102; //double 8 byte
		byte d = 12; //1 byte
		short e = 12; //2 byte
		String f = "Java Full Stack"; //String
		char g = 'a';
		boolean h = true;
	}
}
